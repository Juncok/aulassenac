# Faça um programa que leia dois números e mostre a soma, a subtração, a multiplicação e a divisão entre eles.

num01 = int(input('Digite um número: '))
num02 = int(input('Digite um número: '))

print(f'A soma entre {num01} e {num02} é: {num01 + num02}')
print(f'A subtração entre {num01} e {num02} é: {num01 - num02}')
print(f'A multiplicação entre {num01} e {num02} é: {num01 * num02}')
print(f'A divisão entre {num01} e {num02} é: {num01 / num02}')