//Arredondando valor

valor_aleatório = parseFloat(prompt('Digite um valor: '))
console.log('O valor ' + valor_aleatório + ' arredondado pra cima é: ' + Math.ceil(valor_aleatório))
console.log('O valor ' + valor_aleatório + ' arredondado pra baixo é: ' + Math.floor(valor_aleatório))
console.log('O valor ' + valor_aleatório + ' arredondado normalmente é: ' + Math.round(valor_aleatório))